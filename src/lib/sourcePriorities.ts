// Health monitoring class
export class SourceHealthMonitor {
  private static instance: SourceHealthMonitor;
  private healthData: Map<string, SourcePriority> = new Map();

  static getInstance(): SourceHealthMonitor {
    if (!SourceHealthMonitor.instance) {
      SourceHealthMonitor.instance = new SourceHealthMonitor();
    }
    return SourceHealthMonitor.instance;
  }

  updateHealthStatus(sourceId: string, status: 'healthy' | 'degraded' | 'down', responseTime?: number, error?: string): void {
    const source = SOURCE_PRIORITIES[sourceId];
    if (!source) return;

    const now = new Date();
    const updatedSource = {
      ...source,
      healthStatus: status,
      lastChecked: now,
      averageResponseTime: responseTime ? (source.averageResponseTime + responseTime) / 2 : source.averageResponseTime,
      errorCount: error ? source.errorCount + 1 : source.errorCount,
      lastError: error || source.lastError,
      successRate: status === 'healthy' ? Math.min(100, source.successRate + 1) : Math.max(0, source.successRate - 5)
    };

    this.healthData.set(sourceId, updatedSource);
    SOURCE_PRIORITIES[sourceId] = updatedSource;
  }

  getHealthStatus(sourceId: string): SourcePriority | null {
    return this.healthData.get(sourceId) || SOURCE_PRIORITIES[sourceId] || null;
  }

  getAllHealthStatuses(): SourcePriority[] {
    return Array.from(this.healthData.values());
  }

  getUnhealthySources(): SourcePriority[] {
    return this.getAllHealthStatuses().filter(source => 
      source.healthStatus === 'down' || source.healthStatus === 'degraded'
    );
  }

  getSourcesNeedingCheck(): string[] {
    const now = new Date();
    return Object.keys(SOURCE_PRIORITIES).filter(sourceId => {
      const source = SOURCE_PRIORITIES[sourceId];
      if (!source.enabled) return false;
      
      const lastChecked = source.lastChecked;
      if (!lastChecked) return true;
      
      const timeSinceLastCheck = now.getTime() - lastChecked.getTime();
      const monitoringInterval = this.getMonitoringInterval(source.monitoring);
      
      return timeSinceLastCheck > monitoringInterval;
    });
  }

  private getMonitoringInterval(monitoring: 'daily' | 'weekly' | 'monthly'): number {
    switch (monitoring) {
      case 'daily': return 24 * 60 * 60 * 1000; // 24 hours
      case 'weekly': return 7 * 24 * 60 * 60 * 1000; // 7 days
      case 'monthly': return 30 * 24 * 60 * 60 * 1000; // 30 days
      default: return 24 * 60 * 60 * 1000;
    }
  }
}

