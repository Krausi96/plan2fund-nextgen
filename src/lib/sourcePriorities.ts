// Get sources by priority
export function getSourcesByPriority(priority: 'high' | 'medium' | 'low'): string[] {
  return Object.entries(SOURCE_PRIORITIES)
    .filter(([_, config]) => config.priority === priority)
    .map(([sourceId, _]) => sourceId);
}

// Get sources by monitoring frequency
export function getSourcesByMonitoring(monitoring: 'daily' | 'weekly' | 'monthly'): string[] {
  return Object.entries(SOURCE_PRIORITIES)
    .filter(([_, config]) => config.monitoring === monitoring)
    .map(([sourceId, _]) => sourceId);
}

// Get sources by category
export function getSourcesByCategory(category: string): string[] {
  return Object.entries(SOURCE_PRIORITIES)
    .filter(([_, config]) => config.category === category)
    .map(([sourceId, _]) => sourceId);
}

// Get enabled sources
export function getEnabledSources(): string[] {
  return Object.entries(SOURCE_PRIORITIES)
    .filter(([_, config]) => config.enabled)
    .map(([sourceId, _]) => sourceId);
}

// Get source configuration
export function getSourceConfig(sourceId: string): SourcePriority | null {
  return SOURCE_PRIORITIES[sourceId] || null;
}

// Get monitoring schedule
export function getMonitoringSchedule(): { [frequency: string]: string[] } {
  return {
    daily: getSourcesByMonitoring('daily'),
    weekly: getSourcesByMonitoring('weekly'),
    monthly: getSourcesByMonitoring('monthly')
  };
}

// Get category groups
export function getCategoryGroups(): { [category: string]: string[] } {
  const groups: { [category: string]: string[] } = {};
  
  for (const [sourceId, config] of Object.entries(SOURCE_PRIORITIES)) {
    if (!groups[config.category]) {
      groups[config.category] = [];
    }
    groups[config.category].push(sourceId);
  }
  
  return groups;
}
